public class Iteamlist {
    String name,image,link;

    public Iteamlist(String name, String image, String link) {
        this.name=name;
        this.image=image;
        this.link=link;
    }

    public String getName() {
        return name;
    }

    public String getImage() {
        return image;
    }

    public String getLink() {
        return link;
    }
}
